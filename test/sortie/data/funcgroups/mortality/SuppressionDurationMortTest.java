  public void testReadV6ParFile() {
    GUIManager oManager = null;
    String sFileName = null;
    String sNewFileName = "test7.xml";
    try {

      oManager = new GUIManager(null);

      oManager.clearCurrentData();
      sFileName = write6XMLValidFile();
      oManager.convertPre7ParameterFile(sFileName, sNewFileName);
      oManager.inputXMLParameterFile(sNewFileName);
      MortalityBehaviors oMortBeh = oManager.getMortalityBehaviors();
      ArrayList<Behavior> p_oBehs = oMortBeh.getBehaviorByParameterFileTag("SuppressionDurationMortality");
      assertEquals(1, p_oBehs.size());
      SuppressionDurationMort oMort = (SuppressionDurationMort) p_oBehs.get(0);

      assertEquals(0.25, ((Float)oMort.mp_fSuppDurrMortMax.getValue().get(0)).floatValue(), 0.0001);
      assertEquals(1.0, ((Float)oMort.mp_fSuppDurrMortMax.getValue().get(2)).floatValue(), 0.0001);
      
      assertEquals(20.0, ((Float)oMort.mp_fSuppDurrMortX0.getValue().get(0)).floatValue(), 0.0001);
      assertEquals(5.0, ((Float)oMort.mp_fSuppDurrMortX0.getValue().get(2)).floatValue(), 0.0001);
      
      assertEquals(-3, ((Float)oMort.mp_fSuppDurrMortXb.getValue().get(0)).floatValue(), 0.0001);
      assertEquals(-1, ((Float)oMort.mp_fSuppDurrMortXb.getValue().get(2)).floatValue(), 0.0001);
    }
    catch (ModelException oErr) {
      fail("Parameter file read failed with message " + oErr.getMessage());
    }
    catch (IOException oE) {
      fail("Caught IOException.  Message: " + oE.getMessage());
    } finally {
      new File(sNewFileName).delete();
      new File(sFileName).delete();
    }
  }
  
