  public void testReadV6ParFile() {
    GUIManager oManager = null;
    String sFileName = null;
    String sNewFileName = "test7.xml";
    try {

      oManager = new GUIManager(null);

      oManager.clearCurrentData();
      sFileName = write6XMLValidFile();
      oManager.convertPre7ParameterFile(sFileName, sNewFileName);
      oManager.inputXMLParameterFile(sNewFileName);
      MortalityBehaviors oMortBeh = oManager.getMortalityBehaviors();
      ArrayList<Behavior> p_oBehs = oMortBeh.getBehaviorByParameterFileTag("AggregatedMortality");
      assertEquals(1, p_oBehs.size());
      AggregatedMortality oMort = (AggregatedMortality) p_oBehs.get(0);

      assertEquals(10, oMort.m_fAggMortEpisodeReturnInterval.getValue(), 0.0001);
      assertEquals(0.25, oMort.m_fAggMortPropToKill.getValue(), 0.0001);
      assertEquals(6, oMort.m_fAggMortNumTreesToAggregate.getValue(), 0.0001);
      assertEquals(1, oMort.m_iAggMortClumpDeterministic.getValue(), 0.0001);    
    }
    catch (ModelException oErr) {
      fail("Parameter file read failed with message " + oErr.getMessage());
    }
    catch (IOException oE) {
      fail("Caught IOException.  Message: " + oE.getMessage());
    } finally {
      new File(sNewFileName).delete();
      new File(sFileName).delete();
    }
  }
  
