  /**
   * Tests ValidateData().
   */
  public void testValidateData() {
    GUIManager oManager = null;
    TreePopulation oPop;
    String sFileName = null;
    try {

      //Amount to cut not a proportion
      try {
        oManager = new GUIManager(null);

        //Valid file 1
        oManager.clearCurrentData();
        sFileName = writeNeutralFile();
        oManager.inputXMLParameterFile(sFileName);
        oPop = oManager.getTreePopulation();
        MortalityBehaviors oMortBeh = oManager.getMortalityBehaviors();
        AggregatedMortality oMort = (AggregatedMortality) oMortBeh.createBehaviorFromParameterFileTag("AggregatedMortality");
        oMort.addSpeciesTypeCombo(new SpeciesTypeCombo(0, 3, oPop));
        //Should be OK
        oMort.m_fAggMortPropToKill.setValue( (float) 0.5);
        oMortBeh.validateData(oManager.getTreePopulation());
        //Set to bad value
        oMort.m_fAggMortPropToKill.setValue( (float) 1.5);
        oMortBeh.validateData(oManager.getTreePopulation());
        fail(
            "Mortality validation failed to catch bad aggregated mortality amount to cut.");
      }
      catch (ModelException oErr) {
        ;
      }

      //Return interval is negative
      try {
        oManager = new GUIManager(null);

        //Valid file 1
        oManager.clearCurrentData();
        sFileName = writeNeutralFile();
        oPop = oManager.getTreePopulation();
        oManager.inputXMLParameterFile(sFileName);
        MortalityBehaviors oMortBeh = oManager.getMortalityBehaviors();
        AggregatedMortality oMort = (AggregatedMortality) oMortBeh.createBehaviorFromParameterFileTag("AggregatedMortality");
        oMort.addSpeciesTypeCombo(new SpeciesTypeCombo(0, 3, oPop));
        //Should be OK
        oMort.m_fAggMortEpisodeReturnInterval.setValue(5);
        oMortBeh.validateData(oManager.getTreePopulation());
        //Set to bad value
        oMort.m_fAggMortEpisodeReturnInterval.setValue((float)- 1.5);
        oMortBeh.validateData(oManager.getTreePopulation());
        fail(
            "Mortality validation failed to catch bad aggregated mortality return interval.");
      }
      catch (ModelException oErr) {
        ;
      }
    }
    catch (java.io.IOException oE) {
      fail("Caught IOException.  Message: " + oE.getMessage());
    }
    finally {
      new File(sFileName).delete();
    }

    System.out.println("Mortality ValidateData testing succeeded.");
  }

